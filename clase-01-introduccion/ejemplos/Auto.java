
// Clase Auto
// Representa un auto del mundo real

public class Auto {

    // Atributos (características)
    String marca;
    String modelo;
    int velocidad;

    // Método (comportamiento)
    void mostrarDatos() {
        System.out.println("Marca: " + marca);
        System.out.println("Modelo: " + modelo);
        System.out.println("Velocidad: " + velocidad + " km/h");
    }
}
